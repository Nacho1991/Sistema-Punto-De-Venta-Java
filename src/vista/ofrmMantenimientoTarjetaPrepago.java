    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (this.datos.length > 0) {
            int fila = this.tblTarjetas.getSelectedRow();
            if (fila >= 0) {
                String pCedulaAnterior = this.datos[fila][0];
                frmEdicionTarjetas ofrm = new frmEdicionTarjetas(null, true,this.datos[fila][0], this.datos[fila][1], this.datos[fila][2],Double.parseDouble(this.datos[fila][3]), Double.parseDouble(this.datos[fila][4]));
                ofrm.setVisible(true);
                if (ofrm.isAceptar()) {
                    Tarjeta_Cliente_Prepago oTarjeta = new Tarjeta_Cliente_Prepago(ofrm.getTxtCodigo(),ofrm.getTxtCedula(),ofrm.getTxtCliente(),ofrm.getSpnSaldo(),ofrm.getSpnPorcentaje());
                    Tarjeta_Cliente_PrepagoD oTarjetaD = new Tarjeta_Cliente_PrepagoD(conexion);
                    oTarjetaD.actualizarTarjetaPrepago(oTarjeta, pCedulaAnterior);
                    if (oTarjetaD.isError()) {
                        JOptionPane.showMessageDialog(null,
                            "Error consultando a la base de datos, detalle técnico:" + oTarjetaD.getErrorMsg());
                    } else {
                        this.refrescar();
                    }
                }
            }
         }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (this.datos.length > 0) {
            int respuesta = JOptionPane.showConfirmDialog(rootPane,"¿Esta seguro que desealo Eliminarlo? Si o No?", "Confirmación" , JOptionPane.YES_NO_OPTION);
            if (respuesta == JOptionPane.YES_OPTION) {
            int fila = this.tblTarjetas.getSelectedRow();
            if (fila >= 0) {
                    Tarjeta_Cliente_Prepago pTarjeta = new Tarjeta_Cliente_Prepago(this.datos[fila][0], this.datos[fila][1], this.datos[fila][2], Double.parseDouble(this.datos[fila][3]), Double.parseDouble(this.datos[fila][4]));
                    Tarjeta_Cliente_PrepagoD oTarjetaD = new Tarjeta_Cliente_PrepagoD(conexion);
                    oTarjetaD.borrarTarjetaPrepago(pTarjeta);
                    if (oTarjetaD.isError()) {
                    JOptionPane.showMessageDialog(null,
                            "Error consultando a la base de datos, detalle técnico:" + oTarjetaD.getErrorMsg());
                    } else {
                        this.refrescar();
                    }
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

   
