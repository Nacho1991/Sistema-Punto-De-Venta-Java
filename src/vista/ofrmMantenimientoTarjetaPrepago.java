    private void refrescar() {
        Tarjeta_Cliente_PrepagoD pTarjeta = new Tarjeta_Cliente_PrepagoD(this.conexion);
        ArrayList tarjetas = (ArrayList) pTarjeta.consultarTarjetaPrepago();
        if (pTarjeta.isError()) {
            JOptionPane.showMessageDialog(null,
                    "Error consultando a la base de datos, detalle técnico:" + pTarjeta.getErrorMsg());
        } else {
            this.datos = new String[tarjetas.size()][5];
            for (int i = 0; i < tarjetas.size(); i++) {
                Tarjeta_Cliente_Prepago aux = (Tarjeta_Cliente_Prepago) tarjetas.get(i);
                this.datos[i][0] = aux.getCodigo();
                this.datos[i][1] = aux.getCedula_cliente();
                this.datos[i][2] = aux.getNombre_cliente();
                this.datos[i][3] = String.valueOf(aux.getSaldo_actual());
                this.datos[i][4] = String.valueOf(aux.getPorcentaje_descuento());
                
            }
            this.modelo.setDataVector(datos, cabeceras);
            this.tblTarjetas.setModel(modelo);
        }
    }       
